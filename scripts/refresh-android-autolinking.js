#!/usr/bin/env node
/**
 * Regenerate android/build/generated/autolinking/autolinking.json from .env flags
 * and voxo-autolink gradle snippets (gated native modules).
 */
const fs = require("fs");
const path = require("path");
const { loadEnv } = require("./load-env");

const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "android", "build", "generated", "autolinking");
const OUT_FILE = path.join(OUT_DIR, "autolinking.json");
const SETTINGS_SNIPPET = path.join(ROOT, "android", "voxo-autolink-settings.gradle");
const APP_DEPS_SNIPPET = path.join(ROOT, "android", "app", "voxo-autolink-dependencies.gradle");
const SETTINGS_GRADLE = path.join(ROOT, "android", "settings.gradle");
const APP_BUILD_GRADLE = path.join(ROOT, "android", "app", "build.gradle");
const SETTINGS_MARKER = "// voxo-autolink-settings";
const APP_DEPS_MARKER = "// voxo-autolink-dependencies";

function cleanseName(name) {
  return name.replace(/[~*!'()]+/g, "_").replace(/^@([\w.-]+)\//, "$1_");
}

function patchSettingsGradle() {
  if (!fs.existsSync(SETTINGS_GRADLE)) return;
  let body = fs.readFileSync(SETTINGS_GRADLE, "utf8");
  const applyLine = `apply from: new File(rootDir, "voxo-autolink-settings.gradle")`;
  if (body.includes(SETTINGS_MARKER)) return;
  body = body.trimEnd() + `\n\n${SETTINGS_MARKER}\n${applyLine}\n`;
  fs.writeFileSync(SETTINGS_GRADLE, body);
}

function patchAppBuildGradle() {
  if (!fs.existsSync(APP_BUILD_GRADLE)) return;
  let body = fs.readFileSync(APP_BUILD_GRADLE, "utf8");
  const applyLine = "apply from: \"./voxo-autolink-dependencies.gradle\"";
  if (body.includes(APP_DEPS_MARKER)) return;
  const anchor = "dependencies {";
  if (!body.includes(anchor)) return;
  body = body.replace(anchor, `${APP_DEPS_MARKER}\n${applyLine}\n\n${anchor}`);
  fs.writeFileSync(APP_BUILD_GRADLE, body);
}

function writeAppModuleDependencies(dependencies) {
  const impl = [];
  for (const dep of Object.values(dependencies)) {
    if (!dep.platforms?.android?.sourceDir) continue;
    impl.push(`    implementation project(':${cleanseName(dep.name)}')`);
  }
  const body = [
    "// Generated by scripts/refresh-android-autolinking.js",
    "dependencies {",
    ...impl,
    "}",
    ""
  ].join("\n");
  fs.writeFileSync(APP_DEPS_SNIPPET, body);
  patchAppBuildGradle();
}

function writeSettingsIncludes(dependencies) {
  const lines = [
    "// Generated by scripts/refresh-android-autolinking.js — RN modules gated by .env",
    ""
  ];
  for (const dep of Object.values(dependencies)) {
    const android = dep.platforms?.android;
    if (!android?.sourceDir) continue;
    const projectName = cleanseName(dep.name);
    const rel = path
      .relative(path.join(ROOT, "android"), android.sourceDir)
      .replace(/\\/g, "/");
    lines.push(`include ':${projectName}'`);
    lines.push(
      `project(':${projectName}').projectDir = new File(rootDir, '${rel}')`
    );
    lines.push("");
  }
  fs.writeFileSync(SETTINGS_SNIPPET, lines.join("\n"));
  patchSettingsGradle();
}

async function refreshAndroidAutolinking() {
  if (!fs.existsSync(path.join(ROOT, "android"))) {
    console.warn("[refresh-autolinking] skip — no android/");
    return false;
  }

  loadEnv();

  const { createReactNativeConfigAsync } = require(
    "expo-modules-autolinking/build/reactNativeConfig/reactNativeConfig"
  );

  const result = await createReactNativeConfigAsync({
    platform: "android",
    projectRoot: ROOT,
    searchPaths: [path.join(ROOT, "node_modules")]
  });

  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.writeFileSync(OUT_FILE, JSON.stringify(result));
  writeSettingsIncludes(result.dependencies);
  writeAppModuleDependencies(result.dependencies);

  const names = Object.keys(result.dependencies);
  console.log(
    `[refresh-autolinking] wrote ${names.length} deps → autolinking.json + voxo-autolink-settings.gradle`
  );
  console.log(
    `[refresh-autolinking] telephony=${names.includes("react-native-callkeep")} notifications=${names.includes("@notifee/react-native")} meetings=${names.includes("@daily-co/react-native-daily-js")} chat=${names.includes("@giphy/react-native-sdk")}`
  );
  return true;
}

module.exports = { refreshAndroidAutolinking };

if (require.main === module) {
  refreshAndroidAutolinking().catch((e) => {
    console.error("[refresh-autolinking]", e);
    process.exit(1);
  });
}
